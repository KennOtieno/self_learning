void main() {
  var isSunny = true;
  var isWarm = false;

  print(isSunny && isWarm);

  print(isSunny || isWarm);

  print(!isSunny);
}
/*
      Logical Operators
Declare two boolean variables: isSunny = true, isWarm = false.
Use &&, ||, and ! to print the results of different logical expressions.
                EXPECTED OUTPUT
                false
                true
                false
 */